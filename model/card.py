class Card(object):
    def __init__(self, id=None, name=None, status=None, content=None, category=None,
                 author=None, created_time=None, modified_time=None):
        self.id = id
        self.name = name,
        self.status = status,
        self.content = content,
        self.category = category,
        self.author = author,
        self.created_time = created_time,
        self.modified_time = modified_time
